#include <iostream>
#include <cmath>
using namespace std;

int main() {
    int myValue = 4;
    // finding the square root
    cout << "The the Square root of " << myValue << " is " << sqrt(myValue) << endl;
    // finding the power or a number
    cout << "In the power of 4 is " << pow(myValue, 4) << endl;

    // Triagonometry
    cout << "sin("<< myValue <<") = " << sin(myValue) << endl;
    cout << "cos("<< myValue <<") = " << cos(myValue) << endl;
    cout << "tan("<< myValue <<") = " << tan(myValue) << endl;
    return 0;
}